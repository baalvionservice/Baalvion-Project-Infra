'use strict';
/**
 * gen-rls-migration.js — emit a fail-closed RLS tenant-isolation migration for a
 * set of tenant-scoped tables, using the canonical policy SQL from
 * @baalvion/tenancy so the policy text never drifts per service.
 *
 * Usage:
 *   node scripts/gen-rls-migration.js <schema> <table...> [--tenant-column=<col>]
 *
 * Example (trade-service hot tables):
 *   node scripts/gen-rls-migration.js trade \
 *     orders escrows shipments documents payments compliance_cases \
 *     disputes wallets notifications collections
 *
 * The output is a single transaction; redirect it into the service's own
 * migrations directory and apply with that service's migration runner as the
 * MIGRATION (owner) role — NOT baalvion_app.
 */
const { enableRlsSql } = require('../packages/tenancy');

function parseArgs(argv) {
  const positional = [];
  let tenantColumn = 'tenant_id';
  let wrapTransaction = true;
  for (const arg of argv) {
    const m = /^--tenant-column=(.+)$/.exec(arg);
    if (m) { tenantColumn = m[1]; continue; }
    // For runners that already wrap each file in a transaction (e.g.
    // trade-service migrate.js), omit the BEGIN/COMMIT wrapper.
    if (arg === '--no-transaction') { wrapTransaction = false; continue; }
    positional.push(arg);
  }
  const [schema, ...tables] = positional;
  return { schema, tables, tenantColumn, wrapTransaction };
}

function main() {
  const { schema, tables, tenantColumn, wrapTransaction } = parseArgs(process.argv.slice(2));
  if (!schema || tables.length === 0) {
    process.stderr.write('usage: gen-rls-migration <schema> <table...> [--tenant-column=<col>] [--no-transaction]\n');
    process.exit(1);
  }
  const header = `-- Generated by scripts/gen-rls-migration.js — fail-closed tenant RLS (R1).\n`
    + `-- schema=${schema} tenantColumn=${tenantColumn} tables=${tables.join(',')}\n`
    + `-- Apply as the MIGRATION/owner role; the runtime connects as baalvion_app.\n`;
  const body = tables.map((t) => enableRlsSql(schema, t, { tenantColumn })).join('\n\n');
  const sql = wrapTransaction ? `BEGIN;\n\n${body}\n\nCOMMIT;\n` : `${body}\n`;
  process.stdout.write(`${header}\n${sql}`);
}

main();
